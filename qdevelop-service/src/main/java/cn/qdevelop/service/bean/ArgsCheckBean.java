package cn.qdevelop.service.bean;

public class ArgsCheckBean {
	String[] args;
	boolean isSelected,isAutoSearch,isFullParam;
	public String[] getArgs() {
		return args;
	}
	public void setArgs(String[] args) {
		this.args = args;
	}
	public boolean isSelected() {
		return isSelected;
	}
	public void setSelected(boolean isSelected) {
		this.isSelected = isSelected;
	}
	public boolean isAutoSearch() {
		return isAutoSearch;
	}
	public void setAutoSearch(boolean isAutoSearch) {
		this.isAutoSearch = isAutoSearch;
	}
	public boolean isFullParam() {
		return isFullParam;
	}
	public void setFullParam(boolean isFullParam) {
		this.isFullParam = isFullParam;
	}
	
	
	
	
}
